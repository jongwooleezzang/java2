package org.kh.site;
//산술연산(Arithmetic) <-> 논리 연산 (Logical)
public class Operation2 {

	public static void main(String[] args) {
// 산술연산자 5가지    +,-,*,/, %,
		int su1 = 98;
		int su2 = 18;
		System.out.println("su1+su2:"+(su1+su2));
		System.out.println("su1-su2:"+(su1-su2));
		System.out.println("su1*su2:"+su1*su2);
		System.out.println("su1/su2:"+su1/su2);
		System.out.println("su1%su2:"+su1%su2);
		
	}

}
