		StringBuffer sb = new StringBuffer(""); //스택(stack) 구조
		byte[] b = str.getBytes();
		for(byte c:b) {//문자배열을 문자열로 변경
			char k = (char) c;
			sb.append(k);//한문자씩 ㄴ 
			}
		System.out.println(sb);
		
